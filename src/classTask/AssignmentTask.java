package classTask;

public class AssignmentTask {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("a=" + a);

        a += 90;
        System.out.println("a=" + a);

        a -= 25;
        System.out.println("a=" + a);

        a /= 3;
        System.out.println("a=" + a);

        a %= 10;
        System.out.println("a=" + a);

        a *= (-3);
        System.out.println("a=" + a);

        a++;
        System.out.println("a=" + a);

        a--;
        System.out.println("a=" + a);


    }
}
